-- ==========================================
-- 箭头函数测试
-- ==========================================

-- ==========================================
-- Test A1: 箭头函数语句 -> 基础（有参数）
-- ==========================================
local arrow_stmt = ->(a, b){ print("Test A1 (-> stmt):", a + b) }
arrow_stmt(10, 20)  -- 期望输出: 30

-- ==========================================
-- Test A2: 箭头函数语句 -> 无参数
-- ==========================================
local arrow_stmt_noarg = ->{ print("Test A2 (-> stmt no arg): hello") }
arrow_stmt_noarg()

-- ==========================================
-- Test A3: 箭头函数表达式 => 基础
-- ==========================================
local arrow_expr = =>(x, y){ x * y }
print("Test A3 (=> expr):", arrow_expr(5, 6))  -- 期望: 30

-- ==========================================
-- Test A4: 箭头函数表达式 => 无参数
-- ==========================================
local arrow_expr_noarg = =>{ 42 }
print("Test A4 (=> expr no arg):", arrow_expr_noarg())  -- 期望: 42

-- ==========================================
-- Test A5: 箭头函数表达式 => 复杂 body
-- ==========================================
local adder = =>(n){ 1 + n }
print("Test A5 (=> simple):", adder(99))  -- 期望: 100

-- ==========================================
-- Test A6: 箭头函数表达式作为参数
-- ==========================================
local function apply(f, x)
  return f(x)
end
print("Test A6 (=> as arg):", apply(=>(v){ v * 2 }, 7))  -- 期望: 14

-- ==========================================
-- Test A7: 箭头函数语句作为回调
-- ==========================================
local nums = {10, 20, 30}
local sum = 0
local cb = ->(n){ sum = sum + n }
for _, n in ipairs(nums) do
  cb(n)
end
print("Test A7 (-> callback):", sum)  -- 期望: 60

-- ==========================================
-- Test A8: 箭头函数嵌套
-- ==========================================
local nested = =>(a){ =>(b){ a + b } }
local inner = nested(10)
print("Test A8 (nested =>):", inner(5))  -- 期望: 15

-- ==========================================
-- Test A9: 箭头函数表达式带默认值（不支持，测试 vararg）
-- ==========================================
local vararg_fn = =>(...){ 1 + select(1, ...) }
print("Test A9 (=> vararg):", vararg_fn(3))  -- 期望: 4

-- ==========================================
-- Test A10: 箭头函数语句 -> 多条语句
-- ==========================================
local multi_stmt = ->(a){
  local r = a * 2
  r = r + 1
  print("Test A10 (-> multi stmt): inner value =", r)
}
multi_stmt(5)  -- 期望输出: inner value = 11

-- ==========================================
-- Test A11: 箭头函数赋值后直接调用
-- ==========================================
print("Test A11 (IIFE =>):", (=>(x){ x * x })(7))  -- 期望: 49